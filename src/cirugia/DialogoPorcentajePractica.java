    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int mensaje = JOptionPane.showConfirmDialog(null, "¿Desea modificar la practica?", "Mensaje de confirmación", JOptionPane.YES_NO_OPTION);
        if (mensaje == JOptionPane.YES_OPTION) {
            try {
                dialogoImagenPractica.controlador.quitarPractica(indice);
                dialogoImagenPractica.cargarTabla();
            } catch (JSONException ex) {}
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            int mensaje = JOptionPane.showConfirmDialog(null, "¿Desea quitar la practica?", "Mensaje de confirmación", JOptionPane.YES_NO_OPTION);
            if (mensaje == JOptionPane.YES_OPTION) {
                practicaJson.put("porcentaje", comboPorcentaje.getSelectedItem());
                practicaJson.put("cantidad", cantidad.getValue());
                dialogoImagenPractica.controlador.modificarPracticaMedicoAyudante(practicaJson);
                dialogoImagenPractica.cargarTabla();
            }

        } catch (JSONException ex) {
            Logger.getLogger(DialogoPorcentajePractica.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

