    public void cargarValore() throws JSONException {
        JSONArray listaJson = new JSONArray(dialogoImagenPractica.controlador.cirugia.practicajson);
        practicaJson = listaJson.getJSONObject(indice);
        boolean valor = false;
        int ayudante = 0;
        int cantidad = 1;
        String nombre = "";
        String nivel = "";
        String practica = "0";
        String porcentaje = "100";
        String requiereAP = "";
        try {
            valor = Boolean.valueOf(practicaJson.getString("valor"));
        } catch (Exception e) {
        }
        try {
            ayudante = practicaJson.getInt("ayudante");
        } catch (Exception e) {
        }
        try {
            nombre = practicaJson.getString("nombre");
        } catch (Exception e) {
        }
        try {
            nivel = practicaJson.getString("nivel");
        } catch (Exception e) {
        }
        try {
            practica = practicaJson.getString("practica");
            requiereAP = DialogoBuscarPractica.getRequiereAnatomia(practica);
        } catch (Exception e) {
        }
        try {
            porcentaje = practicaJson.getString("porcentaje");
        } catch (Exception e) {
            practicaJson.put("porcentaje", comboPorcentaje.getSelectedItem());
        }
        try {
            cantidad = practicaJson.getInt("cantidad");
        } catch (Exception e) {
            practicaJson.put("cantidad", cantidad);
        }
        labelNombre.setText(practica + ":" + nombre);
        labelNivel.setText("NIVEL:" + nivel);
        labelCantidad.setText("Cant Ayudante: " + ayudante);
        labelOptativaRequiere.setText("Optativa: " + (valor ? "SI" : "NO") + " Req.AP: " + requiereAP);
        comboPorcentaje.setSelectedItem(porcentaje);
        this.cantidad.setValue(cantidad);
    }

